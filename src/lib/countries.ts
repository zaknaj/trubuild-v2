export function getCurrencyForCountry(countryCode: string): string | undefined {
  return COUNTRIES.find((c) => c.code === countryCode)?.currency
}

// Derive unique currencies from countries, with a representative flag for each
export const CURRENCIES = (() => {
  const seen = new Map<string, { code: string; name: string; emoji: string }>()
  for (const country of COUNTRIES) {
    if (!seen.has(country.currency)) {
      seen.set(country.currency, {
        code: country.currency,
        name: country.currencyName,
        emoji: country.emoji,
      })
    }
  }
  return Array.from(seen.values()).sort((a, b) => a.code.localeCompare(b.code))
})()
